void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){ // 버튼 인터럽트
	if(GPIO_Pin == BUTTON1_Pin){
		Flag = true;
	}
	else if(GPIO_Pin == BUTTON2_Pin){
		if(reYelloLightSec - greenLightSec <= 10 ){
			reYelloLightSec++;
			printf("2\n");
		}
	}
	else if(GPIO_Pin == BUTTON3_Pin){
		if(reYelloLightSec - greenLightSec > 2){
			reYelloLightSec--;
			printf("3\n");
		}
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == TIM3) { // TIM3: 1초마다 인터럽트 발생
        // 1초마다 실행됨
        if(secCnt == yelloLightSec){
        	lightMode = 1;
        }
        else if(secCnt == redLightSec){
        	lightMode = 2;
        }
        else if(secCnt == greenLightSec){
        	lightMode = 3;
        }
        else if(secCnt == reYelloLightSec){
        	lightMode = 1;
        	secCnt = -1;
        }
    	secCnt++;
    }
}
