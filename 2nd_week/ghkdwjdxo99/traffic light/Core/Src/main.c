void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance == TIM1)
	{
		static int cnt = 0;
		cnt = (cnt + 1) % 1000;
		if(cnt == 999)
			Traffic_SecondUpdate();
	}
}
void Traffic_SecondUpdate(void)
{
	trafficCount++;

	if(trafficLightState == LIGHT_RED)
	{
		if(trafficCount == RDurationSec + addDurationSec)
		{
			trafficCount = 0;
			trafficLightState = LIGHT_YELLOW;
			RLedVal = 100 + brightness;
			GLedVal = 100 + brightness;
			printf("RLedVal : %d\r\n", RLedVal);
			printf("GLedVal : %d\r\n", GLedVal);
		}
	}
	else if(trafficLightState == LIGHT_YELLOW)
	{
		if(trafficCount == YDurationSec + addDurationSec)
		{
			trafficCount = 0;
			trafficLightState = LIGHT_GREEN;
			RLedVal = 0;
			GLedVal = 100 + brightness;
			printf("RLedVal : %d\r\n", RLedVal);
			printf("GLedVal : %d\r\n", GLedVal);
		}
	}
	else if(trafficLightState == LIGHT_GREEN)
	{
		if(trafficCount == GDurationSec + addDurationSec)
		{
			trafficCount = 0;
			trafficLightState = LIGHT_RED;
			RLedVal = 100 + brightness;
			GLedVal = 0;
			printf("RLedVal : %d\r\n", RLedVal);
			printf("GLedVal : %d\r\n", GLedVal);
		}
	}
}
